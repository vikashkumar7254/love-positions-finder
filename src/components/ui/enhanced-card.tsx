import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "romantic" | "passionate" | "elegant"
  }
>(({ className, variant = "default", ...props }, ref) => {
  const variants = {
    default: "rounded-lg border bg-card text-card-foreground shadow-sm",
    romantic: "rounded-xl bg-gradient-card border border-romantic/20 shadow-[var(--shadow-card)] hover:shadow-[var(--shadow-romantic)] transition-all duration-300",
    passionate: "rounded-xl bg-gradient-passionate/10 border border-passionate/30 shadow-lg hover:shadow-[var(--shadow-romantic)] transition-all duration-300",
    elegant: "rounded-2xl bg-card/80 backdrop-blur-sm border border-border/50 shadow-[var(--shadow-soft)] hover:shadow-[var(--shadow-card)] transition-all duration-300"
  }
  
  return (
    <div
      ref={ref}
      className={cn(variants[variant], className)}
      {...props}
    />
  )
})
Card.displayName = "Card"

